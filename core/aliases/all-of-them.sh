    (echo -e "   ${LIGHT_0}${RSET} $(gphoto2 --get-config='/main/status/Niveau de batterie' 2>/dev/null | grep '25') ${RSET}" &)
    (playnegative)
    return 0
  fi

  (playcritical)
  echo -e "${LIGHT_0_} ${LIGHT_0_} ${LIGHT_0_}"
  (echo $(gphoto2 --get-config='/main/status/Niveau de batterie' | grep 'Current'))
  echo -ne "${RSET}"
