# Another method for 'quick change directories'.
# Add this to your ~/.zshrc, then just enter
# ``cd ..../dir''
# ----------------------------------------------------------------------------
rationalise-dot() {
  if [[ $LBUFFER = *.. ]]; then
    LBUFFER+=/..
  else
    LBUFFER+=.
  fi
}
