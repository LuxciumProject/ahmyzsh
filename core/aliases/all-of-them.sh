  if [[ $? == 0 ]]; then
    (echo -e "   ${LIGHT_1_}${RSET} $(gphoto2 --get-config='/main/status/Niveau de batterie' 2>/dev/null | grep '50') ${RSET}" &)
    (playwarning)
    return 0
  fi

  VACUOUS=$(gphoto2 --get-config='/main/status/Niveau de batterie' 2>/dev/null | grep '25') >/dev/null
